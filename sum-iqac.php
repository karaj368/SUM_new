              <div class="accordion-body">
              
                <p class="text-black">IQAC may be constituted in every institution under the Chairmanship of the Head of the institution with heads of important academic and administrative units and a few teachers and a few distinguished educationists and representatives of local management and stakeholders. The composition of the IQAC may be as follows:  </p>
                               <ul class="list-style-7-unbold margin-30px-bottom">
                      			<li>Chairperson: Head of the Institution </li>
                                <li>Teachers to represent all level (Three to eight) </li>
                                <li>One member from the Management </li>
                                <li>Few Senior administrative officers </li>
                                <li>One nominee each from local society, Students and Alumni </li>
                                <li>One nominee each from Employers /Industrialists/Stakeholders </li>
                                <li>One of the senior teachers as the coordinator/Director of the IQAC The composition of the IQAC will depend on the size and complexity of the institution, accordingly the representation of teachers may vary. </li>
                               </ul>
                               <p class="text-black">It helps the institutions in planning and monitoring. IQAC also gives stakeholders or beneficiaries a cross-sectional participation in the institution’s quality enhancement activities. The guidelines given here are only indicative and will help the institutions for quality sustenance activities. The membership of such nominated members shall be for a period of two years. The IQAC should meet at least once in every quarter. The quorum for the meeting shall be two-third of the total number of members. The agenda, minutes and Action Taken Reports are to be documented with official signatures and maintained electronically in a retrievable format. It is necessary for the members of the IQAC to shoulder the responsibilities of generating and promoting awareness in the institution and to devote time for working out the procedural details.</p>
                               <p class="text-black">While selecting these members several precautions need to be taken. A few of them are listed below:-</p>
                               <ul class="list-style-7-unbold margin-30px-bottom">
                      			<li>It is advisable to choose persons from various backgrounds who have earned respect for integrity and excellence in their teaching and research. Moreover, they should be aware of the ground realities of the institutional environment. </li>
                                <li>They should be known for their commitment to improving the quality of teaching and learning. </li>
                                <li>It is advisable to change the coordinator after two to three years to bring new thoughts and activities in the institution. </li>
                               </ul>
                               

