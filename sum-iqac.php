                <p class="text-black">Academic and Administrative Audit (AAA), are very essential for the excellence in Higher Education. These are interrelated concepts. Thus in order to have a quality oriented academic, there should be a strong administrative background.</p>
                               <ul class="list-style-7-unbold margin-30px-bottom">
                      			<li>Academic Audit </li>
                                <p class="text-black">Academic audit can be understood as a scientific and systematic method of reviewing the quality of academic process in the institution. It is related with the quality assurance and enhancing the quality of academic activities in HEIs.</p>
                                <li>Administrative Audit</li>
                                <p class="text-black">It can be defined as a process of evaluating the efficiency and effectiveness of the administrative procedure. It includes assessment of policies, strategies &amp; functions of the various administrative departments, control of the overall administrative system etc</p>
                               </ul>
                               <p class="text-black">Major objectives of AAA:-</p>
                               
                               <ul class="list-style-7-unbold margin-30px-bottom">
                      			<li>To understand the existing system and assess the strengths and weaknesses of the Departments and Administrative Units and to suggest the methods for improvement and for overcoming the weaknesses. </li>
                                <li>To identify the bottlenecks in the existing administrative mechanisms and to identify the opportunities for academic reforms, administrative reforms and examination reforms etc. </li>
                                <li>To evaluate the optimum utilization of financial and other resources. </li>
                                <li>To suggest the methods for continuous improvement of quality keeping in mind criteria and reports by NAAC and other bodies</li>

                               </ul>
                               
                              

